public class RepeticaoFor {
    public static void main(String[] args) {
        // For = para
        for (int i = 0; i<=10; i++ ){
            System.out.println(i);
        }

        System.out.println("----------------------");
        // outro metodos
        int i, j;
        for (i = 0, j = 9; i<10; i++, j--){
            System.out.println(i +" - "+ j);
        }
    }
}
