public class Variaveis {
    public static void main(String[] args) {
// Tipos numéricos inteiros
        byte idadeByte = 25;
        short salarioCurto = 1500;
        int populacaoCidade = 100000;
        long populacaoMundial = 8000000000L;

        // Tipos numéricos de ponto flutuante
        float altura = 1.75f;
        double pi = 3.141592653589793;

        // Tipo de caractere
        char inicialDoNome = 'W';

        // Tipo lógico
        boolean brasilCampeao = true;

        // Exibindo os valores
        System.out.println("byte: " + idadeByte);
        System.out.println("short: " + salarioCurto);
        System.out.println("int: " + populacaoCidade);
        System.out.println("long: " + populacaoMundial);
        System.out.println("float: " + altura);
        System.out.println("double: " + pi);
        System.out.println("char: " + inicialDoNome);
        System.out.println("boolean: " + brasilCampeao);

    }
}
