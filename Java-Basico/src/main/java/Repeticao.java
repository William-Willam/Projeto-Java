public class Repeticao {
    public static void main(String[] args) {
        // while = enquanto
        int  i = 0;
        while(i <10){
            System.out.println(i);
            i++;
        }
        System.out.println("Fim do loop while");

        // do - while
        do {
            System.out.println(i);
            i++;
        }while(i <= 10);
    }
}
